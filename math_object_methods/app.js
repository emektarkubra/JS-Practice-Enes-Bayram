// Math Objesi Metotları- Window objesi içerisinde bir objedir

// Floor    -> noktalı sayıyı aşağı yuvarlar
// Ceil     -> noktalı sayıyı yukarı yuvarlar
// Round    -> sayıyı en yakın yere yuvarlar (.5 de yukarı)

// Max      -> sayılardan max olanı alır
// Min      -> sayılardan min olanı alır

// Random   -> 0-1 arasında rastgele değer üretir
            // Math.floor(Math.random()*100)

// Abs      -> mutlak almak için
// Sqrt     -> karekök almak için
// Pow      -> üssünü almak için

// ---- PI sayısı (özellik) -> pi değerini almak için



// Floor 

let a=3.5;
console.log(Math.floor(a));

// Ceil 

console.log(Math.ceil(a));

// Round

console.log(Math.round(a));

// Max 

console.log(Math.max(1,9,54,34,76,23));

// Min 

console.log(Math.min(1,9,54,34,76,23));

// Random 

console.log(Math.floor(Math.random()*100));



// Abs 

let b=-3;
console.log(Math.abs(b));


// Sqrt 

let c= 16;
console.log(Math.sqrt(c));

// Pow 

console.log(Math.pow(-2,2));

//PI

console.log(Math.PI);