/* 

            -------ATAMA OPERATÖRLERİ--------

            +=      -> arttırma
            -=      -> azaltma
            *=      -> çarpma
            /=      -> bölme
            %=      -> mod alma
            **=     -> üst alma

*/



//*   +=, -=, *=, /=

let sayi1 = 4;
sayi1+=2; // sayi1=sayi1+2
console.log(sayi1);  //6

let sayi2 = 4;
sayi2-=3;  //sayi=sayi-3
console.log(sayi2); //1

let sayi3 = 4;
sayi3*=5;  //sayi=sayi*3
console.log(sayi3); //20

let sayi4 = 12;
sayi4/=4; //sayi=sayi/4
console.log(sayi4);  //3

//*   %=

let sayi5 = 12;
sayi5%=5; // sayi=sayi%4
console.log(sayi5);

//*   **=

let sayi6 = 4;
sayi6**=2; // sayi=sayi**4
console.log(sayi6);


